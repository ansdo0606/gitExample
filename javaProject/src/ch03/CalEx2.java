package ch03;

public class CalEx2 {

	public static void main(String[] args) {
		int a = 20;
		int b = 8;
		System.out.printf("a+b=%d\n", a+b);
		System.out.printf("a-b=%d\n", a-b);
		System.out.printf("a*b=%d\n", a*b);
		System.out.printf("a/b=%d\n", a/b);
		System.out.printf("a%%b=%d\n", a%b);//%표시는 2개 사용해야 표시됨
	}

}
